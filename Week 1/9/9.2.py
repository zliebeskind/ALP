def maths1():
  num1 = 50
  num2 = 5
  return num1 + num2

def maths2():
  num1 = 50
  num2 = 5
  return num1 - num2

def maths3(num1, num2):
  return num1 * num2

outputNum = maths2()
print(outputNum)

